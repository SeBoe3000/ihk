<pre>Zutaten für 10-15 Kugeln und mehr:
-	1x Limetten-Baba (reicht für 15 Kugeln und 7x7 Dessertring)
-	1x Maracuja-Limetten-Sirup (ein klein bisschen mehr nehmen)
-	1x Mango-Ananas-Kompott (reicht mit 300g Obst für 10 Kugeln und 7x7 Dessertring)
-	2x Kokossahne (einfache Menge reicht knapp für 10 Kugeln knapp / einfache Menge reicht gut für 7x7 Dessertring als Randverzierung. Bei 15 Kugeln etwas mehr nehmen)
-	1x Schokoüberzug

Zusammensetzung:
-	Kugeln: Limetten-Baba (gebacken in Mini Truffles) in Maracuja-Limetten-Sirup einweichen, mit Mango-Ananas-Kompott einfrieren (Mul3D Ø48), danach in Kokossahne einfrieren (Mul3D Ø58) und mit Schokolade überziehen
-	Dessertring: Limetten-Baba (gebacken in 7cm Dessertring) in Maracuja-Limetten-Sirup einweichen, auf 7cm kürzen und einfrieren, Mango-Ananas-Kompott separat einfrieren (7cm Dessertring), stapeln und mit Kokossahne umranden, ggf. einfrieren und mit Schokolade überziehen

Abwandlungen:
-	Mango-Maracuja-Saft statt Mango-, Passionsfruchtpüree dunklen Rum und Malibu
-	Vanilleextrakt oder gemahlene Tonkabohnen statt Vanilleschoten
-	Bei Kompott: Ananas durch Mango ersetzen oder umgekehrt / 5g Gelatine statt Pektin (letztes Mal 9,7 g Gelatine und 3EL Stärke genutzt, war etwas zu viel)
</pre>

